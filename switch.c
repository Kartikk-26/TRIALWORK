
#include<stdio.h>
int main(){
	int day;
	printf("ENTER NUMBER \n");
	scanf("%d",&day);
	switch(day)
	{
		case 1:{
			printf("M\n");
			break;
		}
		case 2:{
			printf("T\n");
			break;	
		}
		case 3:{
			printf("W\n");
			break;	
		}
		default:{
			printf("ENTER THE BETWEEN 1 TO 7 ONLY\n");
			break;
			
		}
			
			
		}
		return 0;
	}

	
